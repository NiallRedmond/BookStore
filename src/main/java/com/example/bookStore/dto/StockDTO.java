package com.example.bookStore.dto;

public class StockDTO {

	private double level;
	private long id;
	
	public StockDTO() {
		super();
	}
	public double getLevel() {
		return level;
	}
	public void setLevel(double level) {
		this.level = level;
	}
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	
	
	
}
